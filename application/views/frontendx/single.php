						<h2  class="post-heading"><a href="single.html">A canção que não termina <br>
há 50 anos, Love Me Do mudava <br>
a história da música e da década<br>
						</a></h2>
						<div class="post-content">
						  <p> Em 5 de outubro de 1962, um novo som ocupava as ondas aéreas da Grã-Bretanha. Era cru, simples, direto e sensual. Love, love me do, cantavam John Lennon e Paul McCartney, you know I love you. Os Beatles surgiam 17 anos depois do fim da Segunda Guerra Mundial. E nada na cultura, na sociedade ou na vida cotidiana em todo o mundo seria o mesmo depois deles.<br>
					      Quando exatamente começaram os anos 1960? Quando JFK anunciou sua candidatura à Presidência, em 31 de janeiro de 1960? Quando Harold Macmillan admitiu que “ventos de mudança” varriam a África colonial, em 3 de fevereiro? Quando a princesa Margaret, fumando feito uma chaminé, anunciou seu noivado com um plebeu, o fotógrafo Anthony Armstrong-Jones, em 26 de fevereiro? Ou quando John Kennedy finalmente venceu a eleição americana por um triz, em 9 de novembro? Alguns negam qualquer tipo de transição até a posse do novo presidente americano, na manhã azul gélida e emocionante de 20 de janeiro de 1961. Até então, dizem, o Ocidente ainda estava sob as garras de uma gerontocracia esclerótica representada por Eisenhower e Kruchev. Uma coisa é certa. Os anos 1950 ­demoraram um pouco para passar ao limbo do tempo perdido.</p>
						  <p>Ao fazer as contas com o passado, nunca subestime a potência de uma frase real­mente boa. Para o poeta Philip Larkin, o annus mirabilis que confirmaria sua descoberta da relação sexual, e daí o início de uma nova década, caiu entre o fim da proibição do livro O ­Amante de Lady Chatterley, de D. H. Lawrence, e o primeiro LP dos Beatles. A partir de Larkin, tornou-se lugar-comum sugerir que os anos 1960 realmente começaram em outubro de 1962. Nos Estados Unidos, por exemplo, 1º de outubro, uma segunda-feira, foi o dia em que o primeiro estudante negro, James Meredith, matriculou-se na Universidade do Mississippi, antes exclusiva para brancos. Os ativistas pelos direitos civis estavam em ação. Esse foi o mês que se tornou um elo entre os dias cinzentos e rígidos dos anos 1950 e a década seguinte do Technicolor.<br>
						    Em outras palavras, estamos comemorando o 50º aniversário de um momento negligenciado. Em janeiro, a primeira gravação dos Beatles, uma canção intitulada My Bonnie, atribuída a Tony Sheridan e os Beat Brothers, foi lançada pela Polydor. Em fevereiro, os Estados Unidos colocaram um astronauta em órbita pela primeira vez, quando ­John Glenn foi lançado de Cabo Canaveral na Friendship 7. Depois de três voltas na Terra, Glenn caiu no Atlântico. A corrida à Lua tinha começado. Em 7 de abril, em um detalhe do fenômeno ainda não conhecido como “cultura popular”, um certo Brian Jones foi apresentado a dois jovens músicos, Michael Philip (“Mick”) Jagger e Keith Richards, em um clube de jazz em Ealing, Londres. Eles formaram uma banda, os Rolling Stones, naquele mesmo ano. Um documentário que será exibido no mês que vem no festival de cinema de Londres é dedicado ao que aconteceu a seguir. Em 16 de abril o ­cantor folk Bob Dylan apresentava pela primeira vez Blowin’ in the Wind em um clube na West 4th Street, em Nova York. <br>
						    1962 também foi um ano crucial na política da Guerra Fria. Em junho começou a segunda etapa da construção do Muro de Berlim. Nos meses seguintes, fugitivos da Alemanha Oriental morreram na “terra de ninguém”, um espaço mortífero com minas terrestres e arame farpado. Marilyn Monroe era encontrada morta em 5 de agosto, vítima de uma superdose de Nembutal. No dia seguinte, detinham Nelson Mandela na África do Sul.<br>
						    O outono se instalava, e os anos 1960 chegavam com o som de uma “gaita de marinheiro” cheia de blues. Em 5 de outubro, uma sexta-feira, a sinceridade crua de operários dos Beatles, misturada com o improviso de Lennon, entrava na corrente sanguínea do mundo. Da noite para o dia, parecia haver um estilo, uma língua e um ritmo novos, aumentando um repertório de possibilidades pessoais, particulares. A pílula anticoncepcional libertava o sexo da tirania sem graça da camisinha. O “gramofone” dos anos 50 tornava-se a “vitrola” ou o toca-disco dos 60. Repentinamente começava a caçada, nas palavras de Lennon e McCartney, de “alguém para amar, alguém novo”.<br>
						    O tempo corre como um rio largo, mas a história vem em estranhas correntezas e torrentes repentinas. Perguntaram ao primeiro-ministro britânico da época, Harold Macmillan, qual seria a mais provável ameaça à estabilidade de seu governo. “Os acontecimentos, caros rapazes, os acontecimentos”, murmurou. Love Me Do mal havia entrado nas últimas posições das 20 Mais quando outubro de 1962 foi dominado por dois acontecimentos que moldariam as futuras décadas. No dia 11, o papa João XXIII abria o Segundo Concílio Vaticano, popularmente conhecido como Vaticano II, que iniciava um processo de liberalização da Igreja. As conse­quências desses debates ainda reverberam pelo papado conservador de Bento XVI. As questões enfrentadas pelo Vaticano II continuam tão centrais para o catolicismo quanto, nas relações internacionais, as armas nucleares para a paz na terra.</p>
						  <p>Na política das superpotências, 1962 continua sendo o ano arquetípico do pós-Guerra. Em 14 de outubro, um dia depois da estreia de Quem Tem Medo de Virginia Woolf?, de Edward Albee, na Broadway (que colocou sob os refletores outra antiga instituição, o casamento), um avião espião americano fotografava quatro lançadores móveis de mísseis soviéticos em Cuba. O mundo prendia a respiração. Seria o Armagedon? Em 28 de outubro, depois de dias de um suspense intolerável, Kruchev prometia “desmontar as armas, encaixotá-las e devolvê-las à União Soviética”. <br>
						    Lentamente, a vida voltava a uma espécie de normalidade, com sementes para o futuro, também. Em 3 de novembro, The New York Times fazia a primeira referência impressa à máquina milagrosa chamada “computador pessoal”. Alguns dias depois, em 7 de novembro, um Richard Nixon nervoso, derrotado na disputa pelo governo da Califórnia, dizia aos repórteres: “Vocês não terão mais Nixon para chutar por aí, porque, senhores, esta é minha última entrevista coletiva”. No mesmo dia, Kruchev anunciava que a retirada dos mísseis soviéticos de Cuba havia terminado.<br>
						    Em retrospectiva, 1962 hoje parece ser a fascinante antecâmara da grande festa que foram os anos 1960, período no qual ferveram as questões com as quais ainda nos debatemos, direitos civis, feminismo radical, imaginação distópica e se­cularização. Mas então o mundo era jovem. Lennon ia completar 22 anos, McCartney tinha apenas 20. E Love Me Do (três palavras, dois acordes básicos e uma gaita de bolso) mudava as mentes e os corações. •</p>
						  <p>Tradução: Luiz Roberto Mendes Gonçalves</p>
